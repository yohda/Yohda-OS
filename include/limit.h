#define INT_MAX			(2147483647)
#define SHRT_MAX		(32767)
#define CHAR_MAX		(127)

#define INT_MIN			(-2147483648)
#define SHRT_MIN		(-32768)
#define CHAR_MIN		(-128)

#define INT_MAX_DIG	(12)

#endif
